    private boolean isValidProductName(String name) {
        return name.chars().allMatch(c -> Character.isLetter(c) || Character.isSpaceChar(c) || c == '-');
    }
    private boolean isValidPrice(String price) {
    return price.matches("\\d+(\\.\\d+)?");
}
